class UserProfile {
  final String name;
  final String college;
  final String avatar;
  final int streak;
  final int followers;
  final int following;
  final int ecoCircle;

  UserProfile({
    required this.name,
    required this.college,
    required this.avatar,
    required this.streak,
    required this.followers,
    required this.following,
    required this.ecoCircle,
  });
}
